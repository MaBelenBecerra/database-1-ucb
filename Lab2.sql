USE data_ucb




CREATE TABLE PELICULAS(
ID			INT IDENTITY(1,1) NOT NULL PRIMARY KEY,
DIRECTOR	VARCHAR(45) NOT NULL,
NOMBRE		VARCHAR(196) NOT NULL,
GENERO		VARCHAR(15) NOT NULL,
);

