CREATE TABLE HORARIO(
ID				INT IDENTITY(1,1) NOT NULL PRIMARY KEY,
FECHA_INICIO	DATETIME NOT NULL,
FECHA_FIN		DATETIME NOT NULL,
ID_PELICULA		INT NOT NULL FOREIGN KEY REFERENCES PELICULAS (ID)
);

INSERT INTO HORARIO (FECHA_INICIO, FECHA_FIN, ID_PELICULA)
VALUES (GETDATE(), GETDATE()+10, 1)



CREATE TABLE SALA(
ID			INT IDENTITY(1,1) NOT NULL PRIMARY KEY,
NOMBRE		VARCHAR(50) NOT NULL,
UBICACION	VARCHAR(5) NOT NULL,
ID_HORARIO	INT NOT NULL FOREIGN KEY REFERENCES HORARIO (ID)
);

